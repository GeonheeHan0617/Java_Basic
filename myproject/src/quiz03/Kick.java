package quiz03;

public class Kick {

	String name;
	int money;
	String hat;
	
	
	Kick(){
		
	}


	public Kick(String name, int money, String hat) {
		super();
		this.name = name;
		this.money = money;
		this.hat = hat;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getMoney() {
		return money;
	}


	public void setMoney(int money) {
		this.money = money;
	}


	public String getHat() {
		return hat;
	}


	public void setHat(String hat) {
		this.hat = hat;
	}
	
	
		
		
	
	
	
}
